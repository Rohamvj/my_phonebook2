public class Person {
    private String name;
    private String family;
    private int PhoneNum;

    public Person(String name, String family, int PhoneNum) {
        this.name = name;
        this.family = family;

    }

    public String getName() {
        return name;
    }

    public String getFamily() {
        return family;
    }

    public int getAge() {
        return PhoneNum;
    }
}


